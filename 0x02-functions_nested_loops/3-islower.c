/**
 * _islower - checks for lowercase charcters
 * @c: character to be checked
 * if -conditional statement
 * _putchar - prints characters in c
 *
 *
 *
 * Return: 1 if character is lowercase, 0 if not
 */
int _islower(int c)
{
if (c >= 'a' && c <= 'z')
{
return (1);
}
else
{
return (0);
}
}
